public class MarksOfThreeSubjects {
    public static void main(String[] args) {

        // Storing marks in int variables
        int subject1Marks = 75;
        int subject2Marks = 68;
        int subject3Marks = 82;

        // Pass/Fail status
        boolean result = true;

        // Printing the values
        System.out.println("Subject 1 Marks: " + subject1Marks);
        System.out.println("Subject 2 Marks: " + subject2Marks);
        System.out.println("Subject 3 Marks: " + subject3Marks);
        System.out.println("Result: " + result);
    }
}
