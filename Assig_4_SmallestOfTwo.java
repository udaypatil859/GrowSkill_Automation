public class SmallestOfTwo {
    public static void main(String[] args) {

        // Declare two numbers
        int num1 = 20;
        int num2 = 40;

        // Compare the numbers using if-else
        if (num1 < num2) {
            System.out.println("The smallest number is: " + num1);
        } else {
            System.out.println("The smallest number is: " + num2);
        }
    }
}
